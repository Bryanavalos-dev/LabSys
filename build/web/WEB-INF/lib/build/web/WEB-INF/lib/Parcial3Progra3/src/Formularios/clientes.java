    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
        // se define en blanco el lbl de mensajes de validacion
        lblMensaje.setText("");
        
        //validacion de nombre
        Pattern p = Pattern.compile("^[A-Za-z]*\\s()[A-Za-z]*$");
        Matcher m = p.matcher(txtNombre.getText());
        if (!m.find()){
            lblMensaje.setText("Ingrese un nombre valido");
            return;       
        }
        
        //validacion de direccion
        if (txtDir.getText().isEmpty()){
            lblMensaje.setText("La direccion no puede ir vacia");
            return;       
        }
        
     
        //validacion de telefono
        p = Pattern.compile("^\\d{4}\\-\\d{4}$");
        m = p.matcher(txtTel.getText());
        
        if (!m.find()){
            lblMensaje.setText("Ingrese un teléfono valido");
            return;       
        }
        
        
        //validacion de correo
        p = Pattern.compile("^([a-z0-9_-]+\\.)*[a-z0-9_-]+@[a-z0-9_-]+(\\.[a-z0-9_-]+)*\\.[a-z]{2,6}$");
        m = p.matcher(txtCorreo.getText());    
        if (!m.find()){
            lblMensaje.setText("Ingrese un Correo Valido");
            return;       
        }
         
        //preparacion para insertar data
        
        String[] cbs = cbDep.getSelectedItem().toString().split("-");
     
            Connection conexion = ConexionSql.getInstancia().getConexion();
            CallableStatement sql = conexion.prepareCall("{call SP_nuevoCliente(?,?,?,?,?,?)}");
            sql.setString(1, txtNombre.getText());
            sql.setString(2, txtDir.getText());
            sql.setString(3, txtTel.getText());
            sql.setString(4, txtCorreo.getText());
            sql.setInt(5, Integer.parseInt(cbs[0]));
            sql.registerOutParameter(6, java.sql.Types.INTEGER);
            sql.execute();
            if (sql.getInt(6)>0)
                JOptionPane.showMessageDialog(null, "Cliente Ingresado con id: "+sql.getInt(6));
            else
                JOptionPane.showMessageDialog(null, "No se pudo ingresas!");
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
            System.out.println(ex.getMessage());
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

